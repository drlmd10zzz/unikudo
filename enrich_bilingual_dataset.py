def normalize_zh(value: str | None) -> str:
    text = unicodedata.normalize("NFKC", value or "")
    text = text.replace("台", "臺")
    return re.sub(r"\s+", "", text)


def clean_english_name(value: str) -> str:
    text = html.unescape(" ".join((value or "").replace("Universerity", "University").split()))
    if text.isupper():
        text = text.title()
    words = [CONNECTORS.get(word, word) for word in text.split()]
    return " ".join(words)


def load_moe_names() -> dict[str, dict[str, Any]]:
    with MOE_NAMES_CSV.open(encoding="utf-8-sig", newline="") as f:
        rows = list(csv.DictReader(f))

    lookup = {}
    for row in rows:
        name_zh = row.get("學校名稱")
        name_en = row.get("學校英文名稱")
        if not name_zh or not name_en:
            continue
        lookup[normalize_zh(name_zh)] = {
            "name_en": clean_english_name(name_en),
            "source": MOE_SOURCE,
        }
    return lookup


def enrich_school(school: dict[str, Any], official_names: dict[str, dict[str, Any]]) -> dict[str, Any]:
    school = dict(school)
    city_fix = CITY_DISTRICT_FIXES.get(int(school["source_id"]))
    if city_fix:
        school["city_or_county"], school["district"] = city_fix

    manual = MANUAL_OFFICIAL_SCHOOL_NAMES.get(int(school["source_id"]))
    if manual:
        school["name_en"] = manual["name_en"]
        school["name_en_source"] = {
            "source_type": "official_website",
            "title": manual["name_en"],
            "url": manual["url"],
        }
    else:
        alias = SCHOOL_ZH_ALIASES.get(school.get("name_zh") or "")
        official = official_names.get(normalize_zh(alias or school.get("name_zh")))
        if official:
            school["name_en"] = official["name_en"]
            school["name_en_source"] = official["source"]

    city = school.get("city_or_county")
    district = school.get("district")
    school["city_or_county_en"] = CITY_EN.get(city) if city else None
    school["district_en"] = DISTRICT_EN.get(district) if district else None
    school["region_en"] = {
        "north": "Northern Taiwan",
        "central": "Central Taiwan",
        "south": "Southern Taiwan",
        "east": "Eastern Taiwan",
        "islands": "Offshore Islands",
    }.get(school.get("region"))
    return school


def split_parenthetical(text: str) -> tuple[str, list[str]]:
    notes = re.findall(r"[\\(（]([^\\)）]+)[\\)）]", text)
    core = re.sub(r"[\\(（][^\\)）]+[\\)）]", "", text)
    return core, notes


def translate_program_name(name_zh: str | None) -> str:
    original = normalize_zh(name_zh)
    if not original:
        return "Program name unavailable"

    core, notes = split_parenthetical(original)
    core = core.replace("【重點產業系所】", "").replace("(重點產業系所)", "")

    group = ""
    group_topic = ""
    group_match = re.match(r"^(.+?(?:學系|系))(.+)組$", core)
    if group_match:
        dept_part = group_match.group(1)
        group_topic = group_match.group(2)
        core = dept_part

    numeric_group = re.search(r"第([123])類組$", core)
    if numeric_group:
        core = core[: numeric_group.start()]
        group = f"Group {numeric_group.group(1)}"

    for zh, en in [
        ("第一類組", "Group 1"),
        ("第二類組", "Group 2"),
        ("第三類組", "Group 3"),
        ("甲組", "Group A"),
        ("乙組", "Group B"),
    ]:
        if core.endswith(zh):
            core = core[: -len(zh)]
            group = en
            break

    prefix = None
    suffix_patterns = [
        ("學士學位學程", "Bachelor Program in"),
        ("學士學程", "Bachelor Program in"),
        ("學士班", "Bachelor Program in"),
        ("英語學士班", "English-taught Bachelor Program in"),
        ("國際學士學位學程", "International Bachelor Program in"),
        ("國際學士班", "International Bachelor Program in"),
        ("學系", "Department of"),
        ("系", "Department of"),
        ("研究所", "Graduate Institute of"),
        ("學程", "Program in"),
        ("專班", "Special Program in"),
        ("組", "Group in"),
    ]
    for suffix, candidate_prefix in suffix_patterns:
        if core.endswith(suffix):
            core = core[: -len(suffix)]
            prefix = candidate_prefix
            break

    translated_core = translate_topic(core)
    if not translated_core:
        translated_core = core

    if prefix:
        if prefix.endswith("in"):
            result = f"{prefix} {translated_core}"
        else:
            result = f"{prefix} {translated_core}"
    else:
        result = translated_core

    if group:
        result = f"{result}, {group}"

    if group_topic:
        result = f"{result}, {translate_topic(group_topic)} Group"

    if notes:
        result = f"{result} ({'; '.join(translate_topic(note) for note in notes)})"

    result = tidy_program_translation(result)
    return result or f"Program in {original}"


def translate_topic(text: str) -> str:
    if not text:
        return ""

    translated_tokens: list[str] = []
    i = 0
    phrases = sorted(PROGRAM_PHRASES.items(), key=lambda item: len(item[0]), reverse=True)
    while i < len(text):
        matched = False
        for zh, en in phrases:
            if text.startswith(zh, i):
                translated_tokens.append(en)
                i += len(zh)
                matched = True
                break
        if matched:
            continue

        char = text[i]
        if char in PROGRAM_SINGLE_CHARS:
            translated_tokens.append(PROGRAM_SINGLE_CHARS[char])
        elif char in PROGRAM_CHAR_FALLBACK:
            translated_tokens.append(PROGRAM_CHAR_FALLBACK[char])
        elif char.isascii():
            match = re.match(r"[A-Za-z0-9&\\.\\-]+", text[i:])
            if match:
                translated_tokens.append(match.group(0))
                i += len(match.group(0))
                continue
            translated_tokens.append(char)
        else:
            translated_tokens.append(char)
        i += 1

    return tidy_program_translation(" ".join(translated_tokens))


def tidy_program_translation(text: str) -> str:
    text = re.sub(r"\s+", " ", text).strip()
    text = text.replace(" and and ", " and ")
    text = text.replace("Department of Department", "Department")
    text = text.replace("College Bachelor Program", "College Bachelor Program")
    text = re.sub(r"\s+,", ",", text)
    text = re.sub(r",\s*,", ",", text)
    return text


def enrich_program(program: dict[str, Any]) -> dict[str, Any]:
    program = dict(program)
    source_name = program.get("department_name_clean_zh") or program.get("department_name_zh")
    program["department_name_en"] = translate_program_name(source_name)
    program["department_name_en_source"] = "draft_glossary_translation"
    return program


def write_json(path: Path, data: Any) -> None:
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")


def write_program_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    fields = [
        "program_id",
        "application_system",
        "admission_track",
        "admission_group",
        "choice_code",
        "school_id",
        "school_source_id",
        "school_name_zh",
        "department_name_zh",
        "department_name_clean_zh",
        "department_name_en",
        "department_name_en_source",
        "is_key_industry_program",
        "is_international_foundation_program",
        "source_pdf_page",
        "source_section",
        "notes",
    ]
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field) for field in fields})


def main() -> None:
    official_names = load_moe_names()

    for filename in ["schools.json", "schools_enriched_with_program_counts.json", "schools_base.json"]:
        path = DATASET_DIR / filename
        if not path.exists():
            continue
        schools = json.loads(path.read_text(encoding="utf-8"))
        enriched = [enrich_school(school, official_names) for school in schools]
        missing = [school["name_zh"] for school in enriched if not school.get("name_en")]
        if missing:
            raise RuntimeError(f"Missing English names in {filename}: {missing}")
        write_json(path, enriched)

    for name in PROGRAM_FILES:
        json_path = DATASET_DIR / f"{name}.json"
        if not json_path.exists():
            continue
        programs = json.loads(json_path.read_text(encoding="utf-8"))
        enriched_programs = [enrich_program(program) for program in programs]
        write_json(json_path, enriched_programs)
        csv_path = DATASET_DIR / f"{name}.csv"
        if csv_path.exists():
            write_program_csv(csv_path, enriched_programs)

    print("Bilingual enrichment complete")


if __name__ == "__main__":
    main()
